    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtCoursesStudentID.getText().isEmpty() || cbSemester.getItemCount() == 0) {
            JOptionPane.showMessageDialog(this, "Student ID or Semester Number Is Missing");
        } else {
            int id = course.getMax();
            int sid = Integer.parseInt(txtCoursesStudentID.getText());
            int semesterNumber = Integer.parseInt(cbSemester.getSelectedItem().toString());
            String course1 = cbCourse1.getSelectedItem().toString();
            String course2 = cbCourse2.getSelectedItem().toString();
            String course3 = cbCourse3.getSelectedItem().toString();
            String course4 = cbCourse4.getSelectedItem().toString();
            String course5 = cbCourse5.getSelectedItem().toString();

            if (course.doesSemesterExist(sid, semesterNumber)) {
                JOptionPane.showMessageDialog(this, "This Student Has Already Taken Semester " + semesterNumber);
            } else {

                if (course.doesCourseExist(sid, "course1", course1)) {
                    JOptionPane.showMessageDialog(this, "This Student Has Already Taken " + course1 + " As A Course");
                } else {

                    if (course.doesCourseExist(sid, "course2", course2)) {
                        JOptionPane.showMessageDialog(this, "This Student Has Already Taken " + course2 + " As A Course");
                    } else {

                        if (course.doesCourseExist(sid, "course3", course3)) {
                            JOptionPane.showMessageDialog(this, "This Student Has Already Taken " + course3 + " As A Course");
                        } else {

                            if (course.doesCourseExist(sid, "course4", course4)) {
                                JOptionPane.showMessageDialog(this, "This Student Has Already Taken " + course4 + " As A Course");
                            } else {

                                if (course.doesCourseExist(sid, "course5", course5)) {
                                    JOptionPane.showMessageDialog(this, "This Student Has Already Taken: " + course5 + " As A Course");
                                } else {
                                    course.insert(id, sid, semesterNumber, course1, course2, course3, course4, course5);
                                    jTable2.setModel(new DefaultTableModel(null, new Object[]{"ID", "Student ID", "Semester", "Course 1", "Course 2", "Course 3", "Course 4", "Course 5"}));
                                    course.getCourseValue(jTable2, "");
                                    clearCourse();
                                }

                            }
                        }
                    }
                }
            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed


    public boolean isEmptyStudent() {
        if (txtStudentName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's Name and Surname Are Missing");
            return false;
        }
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Student's Date Of Birth Is Missing");
            return false;
        }
        if (jDateChooser1.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "Students From Future Are Not Alowed");
            return false;
        }
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's Email Is Missing");
            return false;
        }
        if (!txtEmail.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid Email Address");
            return false;
        }
        if (txtPhoneNumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's Phone Number Is Missing");
            return false;
        }
        if (txtPhoneNumber.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Student's Phone Number Is Too Long");
            return false;
        }
        if (txtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's Address Is Missing");
            return false;
        }
        if (txtParentsName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Parent's Name Is Missing");
            return false;
        }
        if (txtParentsNumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Parent's Phone Number Is Missing");
            return false;
        }
        if (txtParentsNumber.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Parent's Phone Number Is Too Long");
            return false;
        }
        return true;
    }


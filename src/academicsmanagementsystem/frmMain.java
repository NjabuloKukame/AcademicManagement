    private void btnMsSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMsSearchActionPerformed
        // TODO add your handling code here:
        if (txtMsStudentID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Yout Student ID");
        } else {
            int sid = Integer.parseInt(txtMsStudentID.getText());

            if (marksSheet.doesIDExist(sid)) {
                jTable5.setModel(new DefaultTableModel(null, new Object[]{"ID", "Student ID", "Semester", "Course 1", "Score 1", "Course 2", "Score 2", "Course 3", "Score 3", "Course 4", "Score 4", "Course 5", "Score 5", "Average"}));
                marksSheet.getScoreValue(jTable5, sid);
                String gpa = String.valueOf(String.format("%.2f", marksSheet.getGPA(sid)));
                lblGPA.setText("GPA: "+gpa);
            } else {
                JOptionPane.showMessageDialog(this, "No Scores Were Found");
            }

        }
    }//GEN-LAST:event_btnMsSearchActionPerformed


    private boolean isNumeric(String s) {

        try {
            double d = Double.parseDouble(s);

            if (d >= 0.0 && d <= 100.0) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Please Enter A Valid Number, The Range Is Between 0.0 to 100.0");
                return false;
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "" + e);
        }

        return false;
    }
    
  

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:

        if (isEmpty()) {
            Connection con = (Connection) MyConnection.getConnection();
            PreparedStatement ps;

            String username = txtUsername.getText();
            String password = String.valueOf(txtPassword.getPassword());

            try {
                ps = con.prepareStatement("select * from users where username = ? and password = ?");
                ps.setString(1, username);
                ps.setString(2, password);
                ResultSet rs = ps.executeQuery();

                if (rs.next()) {
                    String role = rs.getString("role"); 
                    JOptionPane.showMessageDialog(this, "Login Successful");

                    // Create an instance of frmMain
                    frmMain frmMain = new frmMain();

                    // Set panel visibility based on user role
                    if ("admin".equals(role)) {
                        // Admin has access to all panels
                        frmMain.enableAllPanels();
                    } else if ("faculty_clerk".equals(role)) {
                        // Faculty Clerk has access to Courses, Scores, and Mark Sheet
                        frmMain.enableFacultyClerkPanels();
                    } else if ("student".equals(role)) {
                        // Student has access to Mark Sheet only
                        frmMain.enableStudentPanels();
                    }

                    frmMain.setVisible(true);
                    frmMain.pack();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid Login Details", "Login Failed", 2);
                }

            } catch (SQLException ex) {
                Logger.getLogger(MarksSheet.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnLoginActionPerformed


    private void btnScoresSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnScoresSaveActionPerformed
        // TODO add your handling code here:
        if (!txtScoresStudID.getText().isEmpty()) {

            if (!score.doesIDExist(Integer.parseInt(txtScoresID.getText()))) {

                int sid = Integer.parseInt(txtScoresStudID.getText());
                int semesterNo = Integer.parseInt(txtScoresSem.getText());
                if (!score.doesSidSemesterExist(sid, semesterNo)) {

                    if (isNumeric(txtScore1.getText()) && isNumeric(txtScore2.getText()) && isNumeric(txtScore3.getText()) && isNumeric(txtScore4.getText()) && isNumeric(txtScore5.getText())) {
                        int id = score.getMax();
                        String course1 = txtScoresCourse1.getText();
                        String course2 = txtScoresCourse2.getText();
                        String course3 = txtScoresCourse3.getText();
                        String course4 = txtScoresCourse4.getText();
                        String course5 = txtScoresCourse5.getText();
                        double score1 = Double.parseDouble(txtScore1.getText());
                        double score2 = Double.parseDouble(txtScore2.getText());
                        double score3 = Double.parseDouble(txtScore3.getText());
                        double score4 = Double.parseDouble(txtScore4.getText());
                        double score5 = Double.parseDouble(txtScore5.getText());
                        double average = (score1 + score2 + score3 + score4 + score5) / 5;
                        nf.setMaximumFractionDigits(2);
                        score.insert(id, sid, semesterNo, course1, course2, course3, course4, course5, score1, score2, score3, score4, score5, Double.parseDouble(nf.format(average)));
                        jTable4.setModel(new DefaultTableModel(null, new Object[]{"ID", "Student ID", "Semester", "Course 1", "Score 1", "Course 2", "Score 2", "Course 3", "Score 3", "Course 4", "Score 4", "Course 5", "Score 5", "Average"}));
                        score.getScoreValue(jTable4, "");
                        clearScores();
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Semester " + semesterNo + " Score Already Added");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Score ID Already Exists");
            }

        } else {
            JOptionPane.showMessageDialog(this, "No Student Selected");
        }
    }//GEN-LAST:event_btnScoresSaveActionPerformed

